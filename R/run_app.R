#' Run the Shiny Application
#'
#' @param ... A series of options to be used inside the app.
#'
#' @export
#' @importFrom shiny shinyApp
#' @importFrom golem with_golem_options
run_app <- function(
  ...
) {
  with_golem_options(
    app = shinyApp(
      ui = snapShooteR:::app_ui, 
      server = snapShooteR:::app_server
    ), 
    golem_opts = list(...)
  )
}
